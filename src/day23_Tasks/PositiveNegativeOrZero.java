package day23_Tasks;

public class PositiveNegativeOrZero {

    public static void positiveNegativeZero(int number){
        if(number>0){
            System.out.println(number+" is positive");
        }else if( number<0){
            System.out.println(number+" is negative");
        }else{
            System.out.println(number + " is zero");
        }

    }



}
